    void filltotable(ArrayList<Sanpham> list) {
        mol = (DefaultTableModel) tblSanpham.getModel();
        mol.setRowCount(0);
        for (Sanpham sanpham : list) {
            mol.addRow(sanpham.addtorow());
        }
    }

    void showdata(int i) {
        txtmasp.setText(tblSanpham.getValueAt(i, 1).toString());
        txttensp.setText(tblSanpham.getValueAt(i, 2).toString());
        txtmota.setText(tblSanpham.getValueAt(i, 3).toString());

        String tenLSP = tblSanpham.getValueAt(i, 4).toString().trim().toLowerCase();

        for (int j = 0; j < cbbLoaisp.getItemCount(); j++) {
            if (cbbLoaisp.getItemAt(j).trim().toLowerCase().equals(tenLSP)) {
                cbbLoaisp.setSelectedIndex(j);
                break;
            }
        }
    }

    void loadcombobox() {
        Sanphamdao dao = new Sanphamdao();
        cbbLoaisp.removeAllItems();
        for (String item : dao.getLoaiSanPhamList()) {
            cbbLoaisp.addItem(item); // Chỉ hiển thị tên nhưng có thể tìm theo ID
        }
    }

