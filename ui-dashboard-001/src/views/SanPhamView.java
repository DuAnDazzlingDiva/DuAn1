    private void btnXOAActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int i = tblSanpham.getSelectedRow(); // Lấy dòng đang chọn
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String ma = txtmasp.getText(); // Lấy mã màu từ ô nhập liệu

        int confirm = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa sản phẩm này?",
            "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if (repo.remove(ma) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                filltotable(repo.getall()); // Cập nhật bảng sau khi xóa
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm cần xóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        i = tblSanpham.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Chọn bản ghi cần update");
        } else {
            String ma = txtmasp.getText();
            Sanpham sp = this.readform();
            if (sp != null) {
                if (repo.update(sp, ma) > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                    this.filltotable(repo.getall());
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }
            }
        }
    }

    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        Sanpham sp = this.readform();
        if (sp == null) {
            return; // Validation failed, don't proceed
        }
        if (repo.add(sp) > 0) {
            JOptionPane.showMessageDialog(this, "lưu thành công");
            this.filltotable(repo.getall());
        } else {
            JOptionPane.showMessageDialog(this, "lưu thất bại");
        }
    }

    private void tblSanphamMouseClicked(java.awt.event.MouseEvent evt) {
        // TODO add your handling code here:
        i = tblSanpham.getSelectedRow();
        this.showdata(i);
    }

