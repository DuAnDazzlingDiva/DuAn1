    Sanpham readform() {
        String masp, tensp, mota;
        int idlsp;
        masp = txtmasp.getText();
        tensp = txttensp.getText();
        mota = txtmota.getText();

        // Validation
        if (masp.isEmpty() || tensp.isEmpty() || mota.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return null;
        }

        try {
            idlsp = Integer.parseInt(cbbLoaisp.getSelectedItem().toString()); // Chuyển String → int
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Loại sản phẩm không hợp lệ!");
            return null;
        }

        return new Sanpham(i, masp, tensp, mota, idlsp, Boolean.TRUE);
    }
